def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def distancia(pacman, fantasma):
    """Calcular la distancia entre Pacman y un fantasma."""
    return sqrt((pacman.x - fantasma.x) ** 2 + (pacman.y - fantasma.y) ** 2)

def mejor_direccion(ghost, pacman):
    """Determinar la mejor dirección para que el fantasma se acerque a Pacman."""
    opciones = [
        vector(10, 0),   # Derecha
        vector(-10, 0),  # Izquierda
        vector(0, 10),   # Arriba
        vector(0, -10),  # Abajo
    ]
    distancias = {}

    # Evaluar la distancia que generaría cada movimiento
    for opcion in opciones:
        nueva_pos = ghost + opcion
        if valid(nueva_pos):
            dist = distancia(nueva_pos, pacman)
            distancias[opcion] = dist

    # Devolver la dirección con la distancia mínima
    return min(distancias, key=distancias.get)

def move():
    """Mover a Pacman y a todos los fantasmas."""
    writer.undo()
    writer.write(state['score'])

    clear()

    # Movimiento de Pacman
    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    # Movimiento de los fantasmas
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            # Decidir si moverse de forma inteligente o aleatoria
            if distancia(point, pacman) < 100:  # Fantasma cerca de Pacman
                plan = mejor_direccion(point, pacman)
            else:
                # Moverse aleatoriamente si está lejos
                options = [
                    vector(10, 0),
                    vector(-10, 0),
                    vector(0, 10),
                    vector(0, -10),
                ]
                plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)


def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


